// ==,!=;>,>=,<,<=
public class Relational {
    public static void main(String[] args) {
        int a=4;
        int b=6;
        System.out.println("a>b is"+(a>b));//f
        System.out.println("a>=b is"+(a>=b));//f
        System.out.println("a<b is"+(a<b));//t
        System.out.println("a<=b is"+(a<=b));//t
        System.out.println("a<=b is"+(a<=b));//t
        System.out.println("a==b is"+(a==b));//f
        System.out.println("a!=b is"+(a!=b));//t
    }
    }

    

