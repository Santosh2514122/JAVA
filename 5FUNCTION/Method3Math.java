//math class method
public class Method3Math {
    public static void main(String[] args) {
        int a=4;
        int b=5;
        System.out.println( Math.min(a,b));///min 
        System.out.println( Math.max(a,b));///max
        System.out.println( Math.sqrt(a));///square 
        System.out.println( Math.pow(a,b));///power 
        System.out.println( Math.abs(a));///abs
        //System.out.println( Math.random(a,b));///min 
        System.out.println( Math.floor(a));
        System.out.println( Math.ceil(a));
        System.out.println( Math.round(a));
        

    }
}
